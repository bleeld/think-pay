     * 
     * @param string $ip 客户端IP
     * @param array $whitelist IP白名单（CIDR格式）
     * @return bool
     */
    public static function verify(string $ip, array $whitelist): bool
    {
        if (empty($whitelist)) {
            return true; // 白名单为空时放行
        }

        foreach ($whitelist as $cidr) {
            if (self::ipInRange($ip, $cidr)) {
                return true;
            }
        }

        return false;
    }

